-- Initialize fsu_roster SCHEMA

-- CREATE SCHEMA fsu_roster;

DROP TABLE IF EXISTS
    fsu_roster.Player, fsu_roster.Coach, fsu_roster.Recruit;

CREATE OR REPLACE FUNCTION trigger_set_timestamp()
              RETURNS TRIGGER AS $$
BEGIN
    NEW.LastUpdateDate = NOW();
RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Create the Player table
CREATE TABLE fsu_roster.Player
(
    PlayerId        SERIAL PRIMARY KEY,
    Name            VARCHAR(100) NOT NULL,
    Position        VARCHAR(50) NOT NULL,
    Year            INT NOT NULL,
    Redshirt        BOOLEAN DEFAULT FALSE,
    Jersey          INT NOT NULL,
    Sport           VARCHAR(50) NOT NULL,
    Status          VARCHAR(100) NOT NULL,
    Height          VARCHAR(10),
    Weight          INT,
    HomeTown        VARCHAR(100),
    HighSchool      VARCHAR(100),
    OtherCollege    VARCHAR(100),
    DraftPick       INT,
    NFLTeam         VARCHAR(100),
    Notes           TEXT,
    LastUpdateDate  TIMESTAMP NOT NULL DEFAULT now()
);

CREATE TRIGGER set_player_timestamp
    BEFORE UPDATE ON fsu_roster.Player
    FOR EACH ROW
    EXECUTE PROCEDURE trigger_set_timestamp();

-- Create the Recruit table
CREATE TABLE fsu_roster.Recruit
(
    RecruitId           SERIAL PRIMARY KEY,
    PlayerId            BIGINT NOT NULL REFERENCES fsu_roster.Player (PlayerId),
    RivalsStars         INT,
    RivalsRating        NUMERIC(2, 1),
    RivalsRankNational  INT,
    RivalsRankPosition  INT,
    RivalsRankState     INT,
    RivalsLink          VARCHAR(100),
    LastUpdateDate      TIMESTAMP NOT NULL DEFAULT now()
);

CREATE TRIGGER set_rivals_timestamp
    BEFORE UPDATE ON fsu_roster.Recruit
    FOR EACH ROW
    EXECUTE PROCEDURE trigger_set_timestamp();

-- Create the Coach table
CREATE TABLE fsu_roster.Coach
(
    CoachId         SERIAL PRIMARY KEY,
    Name            VARCHAR(100) NOT NULL DEFAULT '',
    Position        VARCHAR(100) NOT NULL DEFAULT '',
    Sport           VARCHAR(100) NOT NULL DEFAULT '',
    LastUpdateDate  TIMESTAMP NOT NULL DEFAULT now()
);

CREATE TRIGGER set_coach_timestamp
    BEFORE UPDATE ON fsu_roster.Coach
    FOR EACH ROW
    EXECUTE PROCEDURE trigger_set_timestamp();

